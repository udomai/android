@Preview(showBackground = true)
@Composable
fun DiceRollerApp() {
    DiceWithButtonAndImage(
        modifier = Modifier
            .fillMaxSize()
            .background(Color.Black)
            .wrapContentSize(Alignment.Center)
    )
}

@Composable
fun DiceWithButtonAndImage(modifier: Modifier = Modifier) {
    var result by remember { mutableStateOf(1) }
    var rolls by remember { mutableStateOf(0) }
    val resultList = remember { mutableStateListOf<Int>() }
    val avg = String.format("%.2f", if (resultList.size == 0) 0.toDouble() else resultList.sum().toDouble() / resultList.size)
    val imageResource = when (result) {
        1 -> R.drawable.dice_1
        2 -> R.drawable.dice_2
        3 -> R.drawable.dice_3
        4 -> R.drawable.dice_4
        5 -> R.drawable.dice_5
        else -> R.drawable.dice_6
    }
    Column(
        modifier = modifier,
        horizontalAlignment = Alignment.CenterHorizontally
    ) {
        Image(
            painter = painterResource(imageResource),
            contentDescription = "1"
        )
        Spacer(modifier = Modifier.height(80.dp))
        Button(
            onClick = {
                result = (1..6).random()
                rolls++
                resultList.add(result)
            },
            modifier = Modifier
                .size(width = 180.dp, height = 60.dp),
            shape = RectangleShape
        ) {
            Text(
                text = stringResource(R.string.roll),
                fontSize = 40.sp,
                fontFamily = FontFamily.Serif
            )
        }
        Spacer(modifier = Modifier.height(80.dp))
        Text(
            text = "Stats for nerds:\n\nRolls:          ${rolls}\n\nAvg. result:    $avg",
            fontSize = 20.sp,
            fontFamily = FontFamily.Monospace,
            color = Color.White,
            modifier = Modifier
                .fillMaxWidth()
                .padding(16.dp)
        )
    }
}